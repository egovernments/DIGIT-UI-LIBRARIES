  /// Gets an icon by its string key
  /// Returns the default icon if the key is not found
  ///
  /// Example:
  /// ```dart
  /// Icon icon = Icon(DigitIconMapping.getIcon('Home'));
  /// ```
  static IconData getIcon(String key) {
    return iconMap[key] ?? defaultIcon;
  }

  /// Checks if an icon key exists in the mapping
  ///
  /// Example:
  /// ```dart
  /// bool exists = DigitIconMapping.hasIcon('Home'); // returns true
  /// bool exists = DigitIconMapping.hasIcon('InvalidKey'); // returns false
  /// ```
  static bool hasIcon(String key) {
    return iconMap.containsKey(key);
  }

  /// Gets all available icon keys
  ///
  /// Example:
  /// ```dart
  /// List<String> keys = DigitIconMapping.getAllKeys();
  /// ```
  static List<String> getAllKeys() {
    return iconMap.keys.toList();
